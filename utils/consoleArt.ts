export const detectConsoleOpen = (callback: () => void) => {
  let devtools = false

  setInterval(() => {
    if (
      window.outerHeight - window.innerHeight > 200 ||
      window.outerWidth - window.innerWidth > 200
    ) {
      if (!devtools) {
        devtools = true
        callback()
      }
    } else {
      devtools = false
    }
  }, 500)
}
